# and 
# si una de las dos es *falso* todo es falso 
print("AND")
print(True and True) # verdadero
print(True and False) # todo falso si una es falso
print(False and True) # todo falso si una es falso
print(False and False) # todo falso si una es falso

# or
# si una es True todo es verdadero    

print("OR")
print(True or True)  # si una es true todo es true
print(True or False)  # si una es true todo es true
print(False or True)  # si una es true todo es true
print(False or False) # falso

